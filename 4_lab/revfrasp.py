
import numpy as np
import random as rnd

#–°—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å –≤—ã–±–æ—Ä–∫—É –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω—ã—Ö —Ä–∞–≤–Ω–æ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –Ω–∞ –∏–Ω—Ç–µ—Ä–≤–∞–ª–µ (ùëé, ùëè) —Å–ª—É—á–∞–π–Ω—ã—Ö –≤–µ–ª–∏—á–∏–Ω –º–µ—Ç–æ–¥–æ–º –æ–±—Ä–∞—Ç–Ω—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π.
#–û–±—Ä–∞—Ç–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Å–ª—É—á –≤–µ–ª —Ä–∞–≤–Ω–æ–º–µ—Ä–Ω–æ–π X –Ω–∞ (a, b) [10 —Ñ–æ—Ä–º—É–ª–∞ –≤ –º–µ—Ç–æ–¥–∏—á–∫–µ] 
def revraspr(x, a, b):
    return x*(b-a)+a

n = int(input("–í–≤–µ–¥–∏—Ç–µ N: "))
a = float(input("–í–≤–µ–¥–∏—Ç–µ a: "))
b = float(input("–í–≤–µ–¥–∏—Ç–µ b: "))

#–≤—ã–±–æ—Ä–∫–∞ –Ω–µ–∑–∞–≤–∏—Å–∏–º—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π r
r = np.array([rnd.random() for i in range(n)])
x = []
for i in r:
    temp = revraspr(i, a, b)
    x.append(temp)
print("–í—ã–±–æ—Ä–∫–∞ –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω—ã—Ö —Ä–∞—Å–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω—ã—Ö –Ω–∞ –∏–Ω—Ç–µ—Ä–≤–∞–ª–µ –Ω–∞ –∏–Ω—Ç–µ—Ä–≤–∞–ª–µ (ùëé, ùëè) —Å–ª—É—á–∞–π–Ω—ã—Ö –≤–µ–ª–∏—á–∏–Ω –º–µ—Ç–æ–¥–æ–º –æ–±—Ä–∞—Ç–Ω—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π:")
print(x)